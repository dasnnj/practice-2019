package com.dasnnj.practice.readfile.entity;

/**
 * The type Basic quote.
 */
public class BasicQuote {

    /**
     * The Data source id.
     */
    protected String dataSourceID;

    /**
     * The Push date time.
     */
    protected String pushDateTime;

    /**
     * The Type.
     */
    protected String type;

    /**
     * The U uid.
     */
    protected String uUID;

    /**
     * Gets data source id.
     *
     * @return the data source id
     */
    public String getDataSourceID() {
        return dataSourceID;
    }

    /**
     * Sets data source id.
     *
     * @param dataSourceID the data source id
     */
    public void setDataSourceID(String dataSourceID) {
        this.dataSourceID = dataSourceID;
    }

    /**
     * Gets push date time.
     *
     * @return the push date time
     */
    public String getPushDateTime() {
        return pushDateTime;
    }

    /**
     * Sets push date time.
     *
     * @param pushDateTime the push date time
     */
    public void setPushDateTime(String pushDateTime) {
        this.pushDateTime = pushDateTime;
    }

    /**
     * Gets type.
     *
     * @return the type
     */
    public String getType() {
        return type;
    }

    /**
     * Sets type.
     *
     * @param type the type
     */
    public void setType(String type) {
        this.type = type;
    }

    /**
     * Gets uid.
     *
     * @return the uid
     */
    public String getuUID() {
        return uUID;
    }

    /**
     * Sets uid.
     *
     * @param uUID the u uid
     */
    public void setuUID(String uUID) {
        this.uUID = uUID;
    }
}
